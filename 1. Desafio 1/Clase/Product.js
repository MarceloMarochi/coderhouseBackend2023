class Product extends ProductManager {
  constructor(title, description, price, thumbnail, code, stock) {
    this.id = Producto.idProducto();
    this.title = title;
    this.description = description;
    this.price = price;
    this.thumbnail = thumbnail;
    this.code = code;
    this.stock = stock;
  }

  static idProducto() {
    if (this.idIncrement) {
      this.idIncrement++;
    } else {
      this.idIncrement = 1;
    }
    return this.idIncrement;
  }
}
